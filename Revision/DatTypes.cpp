//Program to show all the Data Types
#include<iostream>
using namespace std;
int main(){

    int apples = 5; //integer 
    cout<<apples<<endl;
    cout<<sizeof(apples)<<endl; //sizeof operator

    float num = 9.11; //float or decimal 
    cout<<num<<endl;
    cout<<sizeof(num)<<endl;

    char character = 't'; // character
    cout<<character<<endl;
    cout<<sizeof(character)<<endl;

    bool flag = true; //boolean
    cout<<flag<<endl;
    cout<<sizeof(flag)<<endl;


    return 0;
}