//Program to display all the Relational Operators
#include<iostream>
using namespace std;
int main(){

    int num1 = 2 , num2 = 4;
    cout<<(num1==num2)<<endl; // comparing or double equal to
    cout<<(num1!=num2)<<endl; // not equal to
    cout<<(num1<=num2)<<endl; // less than equal to
    cout<<(num1>=num2)<<endl; // greater than equal to
    return 0;
}